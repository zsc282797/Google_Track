package driver.framework;

/** A command that prints a list of previous commands
 * One command per line, and the highest number points to the most
 * recent command.
 */

import java.util.*;

public class CmdHistory {
  /** constructor of class CmdHistory
   */
  private LinkedList<String> LKL = new LinkedList<String>();
  /**This method add new command to a linklist.
   * @param cmd
   */
  public void add(String cmd){
      LKL.add(cmd);
  }
  /**This method get the value n of a linklist
   * @param n
   * @return value n
   */
  public String get(int n){
    return LKL.get(n);
  }
  /**This method determines the size of a linklist
   * @return size of a linklist
   */
  public int Size(){
    return LKL.size();
  }
  
}

